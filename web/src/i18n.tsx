export type LanguageCode = keyof typeof translations;
export type TranslationKey = keyof typeof translations.en;

export const LANGUAGE_OPTIONS: LanguageCode[] = ['en', 'nl', 'fy'];

export function readLanguageFromPath(pathname: string): LanguageCode | null {
  const [candidate] = pathname.split('/').filter(Boolean);
  if (candidate === 'en' || candidate === 'nl' || candidate === 'fy') {
    return candidate;
  }
  return null;
}

type TranslationContextValue = {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
  t: (key: TranslationKey, variables?: Record<string, string | number>) => string;
};

const TranslationContext = createContext<TranslationContextValue | null>(null);

function readStoredLanguage(): LanguageCode {
  const pathLanguage = readLanguageFromPath(window.location.pathname);
  if (pathLanguage) {
    return pathLanguage;
  }

  try {
    const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
    if (stored === 'en' || stored === 'nl' || stored === 'fy') {
      return stored;
    }
  } catch {
    // Ignore storage failures and use English.
  }

  return 'en';
}

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<LanguageCode>(() => readStoredLanguage());

  useEffect(() => {
    try {
      window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    } catch {
      // Ignore storage failures and keep the UI usable.
    }
  }, [language]);

  const t = (key: TranslationKey, variables?: Record<string, string | number>): string => {
    const template = translations[language][key] ?? translations.en[key];
    if (!variables) {
      return template;
    }

    return Object.entries(variables).reduce((value, [name, replacement]) => {
      return value.replaceAll(`{${name}}`, String(replacement));
    }, template);
  };

  return (
    <TranslationContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </TranslationContext.Provider>
  );
}

export function useTranslation(): TranslationContextValue {
  const context = useContext(TranslationContext);
  if (!context) {
    throw new Error('useTranslation must be used within a LanguageProvider.');
  }
  return context;
}
